-- ============================================================================
-- Window Washer — "Height Lover" (config)
-- Пусть эффект масштабируется под 27-этажное здание (уровни Z: 0..26).
-- Здесь только «константы» — удобно трогать баланс без правки логики.
-- ============================================================================

if not WW_HL then WW_HL = {} end

WW_HL.minZ         = 8     -- ниже этого «этажа» эффект ≈ 0 (пример: с 9-го ощущается высота)
WW_HL.topZ         = 26    -- вершина здания (для 27 этажей: 0..26)
WW_HL.clampMax     = 1.20  -- верхняя «крышка» итогового коэффициента  (вместе с бонусами)

-- Мультипликаторы окружения (контекст)
WW_HL.env = {
  platform = 1.00,  -- на подвесной платформе (эталон)
  outside  = 0.80,  -- крыша / открытый балкон / улица на высоте (редко)
  nearOut  = 0.55,  -- внутри, но рядом с «наружой» (рядом окно/наружная стена)
  inside   = 0.30,  -- глубоко внутри
}

WW_HL.platformBonus = 0.15  -- небольшой «кайф от работы» именно на платформе

-- Как именно масштабировать «долю высоты»:
WW_HL.heightCurve = "quadratic"
-- варианты:
--   "linear"    -> ровная шкала
--   "quadratic" -> низкие этажи мало дают, верхние — ощутимо больше (рекомендуется)

-- Периодичность применения эффекта (минутами внутриигрового времени)
WW_HL.tickMinutes = 10

-- Сила положительного эффекта на высоте (за WW_HL.tickMinutes при factor=1.0)
WW_HL.kBored  = 0.25   -- снимаем скуку (BodyDamage:Boredom)
WW_HL.kUnhap  = 0.20   -- снимаем несчастье (BodyDamage:UnhappynessLevel)
WW_HL.kStress = 0.08   -- снимаем стресс (Stats:Stress, 0..1)

-- «Тяга к высоте» — если давно не было «дозы» высоты,
-- начинаем ПОСТЕПЕННО наращивать скуку/несчастье.
WW_HL.heightTouchFactor = 0.40     -- чем считать «побывал на высоте»
WW_HL.hungerSafeHours   = 24       -- первые 24 часа без высоты — без штрафа
WW_HL.hungerHardHours   = 48       -- после 48 часов — усиленный штраф

-- Сила накопления «голода по высоте» за тик (10 мин) после порогов
WW_HL.hunger_kBored_soft = 0.10    -- 24..48 часов
WW_HL.hunger_kUnhap_soft = 0.08
WW_HL.hunger_kBored_hard = 0.18    -- >48 часов
WW_HL.hunger_kUnhap_hard = 0.14
